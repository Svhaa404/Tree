bool isSame(node* root1, node* root2){
    if(root1==NULL && root2==NULL){
        return true;
    }
    if(root2==NULL|| root1==NULL){
        return false;
    }

return root1->data==root2->data && isSame(root1->left, root2->left)&& isSame(root2->right,root2->right);
}

    

